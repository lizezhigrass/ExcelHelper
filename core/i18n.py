"""
core/i18n.py

轻量级国际化 (i18n) 翻译模块。 / Lightweight internationalization (i18n) translation module.
支持自动检测系统语言（locale），并提供中文到英文的自动映射。 / Supports automatic detection of system language (locale) and provides Chinese-to-English mapping.
"""

import locale
import logging

logger = logging.getLogger(__name__)

# 全局当前语言，默认使用英文初始化 / Global current language, initialized to English by default
CURRENT_LANG = "en"

def detect_system_lang() -> str:
    """自动感知操作系统语言 / Automatically detect operating system language"""
    try:
        lang, _ = locale.getdefaultlocale()
        if lang and "zh" in lang.lower():
            return "zh"
    except Exception as e:
        logger.warning("自动感知系统语言失败，默认使用英文 / Failed to detect system language, fallback to English: %s", e)
    return "en"

# 初始化自动感知 / Initialize automatic language detection
CURRENT_LANG = detect_system_lang()

def set_language(lang: str) -> None:
    """手动调整语言 / Manually adjust interface language"""
    global CURRENT_LANG
    if lang in ("zh", "en"):
        CURRENT_LANG = lang
        logger.info("界面语言手动切换为 / Interface language manually switched to: %s", lang)

def get_language() -> str:
    """获取当前语言 / Get current language"""
    return CURRENT_LANG

def _t(text: str) -> str:
    """
    翻译核心函数。 / Core translation function.
    如果当前语言是中文，则原样返回；如果是英文，则返回其在 TRANSLATIONS 中的映射值；如果没找到，则原样返回。
    / If the current language is Chinese, returns the original text; if English, returns its mapped value in TRANSLATIONS; if not found, returns the original text.
    """
    if CURRENT_LANG == "zh" or not text:
        return text
    return TRANSLATIONS.get(text, text)

